					<p><span class = "first-half-sentence">The band released their sophomore album, Diluvia, on October 9, 2012. </span>On April 17, 2013, the band announced on Facebook that Doris Cellar would be leaving the band. On August 15, 2014, a new song by the band, titled "Hyde", was posted to The Wild Honey Pie's YouTube channel.[7]</p>

